using System; // Loops
class Program
{
    static void Main(string[] args)
    {
        for (int i = 0; i < 10; i++)
        {
            Console.WriteLine("{0}", i);
        }
        Console.WriteLine();
        for (int j = 10; j > 0; j--)
        {
            Console.WriteLine("{0}", j);
        }
    }
}